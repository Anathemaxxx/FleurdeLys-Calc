def rename(names, species)
	grouped = {}

	names.each do |n|
		if !grouped[n.split("-")[0]]
			grouped[n.split("-")[0]] = [n.split("-")[0]]
		else
			if n.split("-")[0] == grouped[n.split("-")[0]][0]
				grouped[n.split("-")[0]].push << n
			else
				grouped[n.split("-")[0]] = [n.split("-")[0]]
			end
		end
	end






	files = Dir['img/hd/*.png'].sort
	files.each do |f|
		idx = f[7..-7].to_i
		form = f[-5].to_i

		begin
			image = grouped[species[idx]][form].downcase
		rescue
			p "error #{[idx, form]}"
			next
		end
		p f
		p image
		`cp #{f} ./img/newhd/#{image}.png`
	end
end


